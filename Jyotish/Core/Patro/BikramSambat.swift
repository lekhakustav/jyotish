/// Bikram Sambat calendar — table-driven conversion (BS 2000–2090).
/// Anchor: BS 2000-01-01 == AD 1943-04-14. Sanity anchors: 2080-01-01 == 2023-04-14,
/// 2081-01-01 == 2024-04-13 (verify after any table edit).
struct NepaliDate: Codable, Equatable, Hashable, Comparable {
    var year: Int   // BS
    var month: Int  // 1 = Baisakh … 12 = Chait
    var day: Int

    static func < (a: NepaliDate, b: NepaliDate) -> Bool {
        (a.year, a.month, a.day) < (b.year, b.month, b.day)
    }

    static let monthNamesEN = ["Baisakh", "Jestha", "Asar", "Shrawan", "Bhadra", "Asoj",
                               "Kartik", "Mangsir", "Poush", "Magh", "Falgun", "Chait"]
    static let monthNamesNE = ["वैशाख", "जेठ", "असार", "साउन", "भदौ", "असोज",
                               "कात्तिक", "मंसिर", "पुष", "माघ", "फागुन", "चैत"]

    func monthName(ne: Bool) -> String {
        ne ? Self.monthNamesNE[month - 1] : Self.monthNamesEN[month - 1]
    }
}


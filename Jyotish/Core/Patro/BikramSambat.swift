    /// AD anchor for BS 2000-01-01.
    static var anchorAD: Date {
        var comps = DateComponents(); comps.year = 1943; comps.month = 4; comps.day = 14; comps.hour = 12
        return Calendar.nepali.date(from: comps)!
    }

    static func daysInMonth(year: Int, month: Int) -> Int {
        guard year >= firstYear, year - firstYear < table.count else { return 30 }
        return table[year - firstYear][month - 1]
    }

    static func toAD(_ bs: NepaliDate) -> Date {
        var days = 0
        for y in firstYear..<bs.year { days += table[y - firstYear].reduce(0, +) }
        for m in 1..<bs.month { days += daysInMonth(year: bs.year, month: m) }
        days += bs.day - 1
        return Calendar.nepali.date(byAdding: .day, value: days, to: anchorAD)!
    }

    static func toBS(_ ad: Date) -> NepaliDate {
        let cal = Calendar.nepali
        var days = cal.dateComponents([.day], from: cal.startOfDay(for: anchorAD), to: cal.startOfDay(for: ad)).day ?? 0
        var year = firstYear
        while true {
            let inYear = table[min(year - firstYear, table.count - 1)].reduce(0, +)
            if days < inYear { break }
            days -= inYear; year += 1
            if year - firstYear >= table.count { break }
        }
        var month = 1
        while days >= daysInMonth(year: year, month: month) {
            days -= daysInMonth(year: year, month: month); month += 1
        }
        return NepaliDate(year: year, month: month, day: days + 1)
    }

    static func today() -> NepaliDate { toBS(Date()) }
